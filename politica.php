      // Script para la navegación interna suave
      document.addEventListener("DOMContentLoaded", function () {
        // Navegación suave
        document.querySelectorAll(".policy-nav a").forEach((anchor) => {
          anchor.addEventListener("click", function (e) {
            e.preventDefault();

            // Remover clase activa de todos los enlaces
            document.querySelectorAll(".policy-nav a").forEach((a) => {
              a.classList.remove("active");
            });

            // Agregar clase activa al enlace clickeado
            this.classList.add("active");

            // Obtener el elemento objetivo
            const targetId = this.getAttribute("href");
            const targetElement = document.querySelector(targetId);

            // Desplazamiento suave
            window.scrollTo({
              top: targetElement.offsetTop - 100,
              behavior: "smooth",
            });
          });
        });

        // Actualizar enlace activo al desplazarse
        window.addEventListener("scroll", function () {
          const sections = document.querySelectorAll(
            "#privacy, #terms, #refunds"
          );
          let currentSection = "";

          sections.forEach((section) => {
            const sectionTop = section.offsetTop;
            if (pageYOffset >= sectionTop - 150) {
              currentSection = section.getAttribute("id");
            }
          });

          document.querySelectorAll(".policy-nav a").forEach((a) => {
            a.classList.remove("active");
            if (a.getAttribute("href") === "#" + currentSection) {
              a.classList.add("active");
            }
          });
        });
      });
    </script>
